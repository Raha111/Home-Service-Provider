    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int index = table.getSelectedRow();
        TableModel model = table.getModel();
        String name = model.getValueAt(index,0).toString();
        String sename = c_service1.getSelectedItem().toString();
        
        int a = JOptionPane.showConfirmDialog(null,"Do you want to remove "+name+" as worker??","select",JOptionPane.YES_NO_OPTION );
        if(a==0){
         try {
            Connection con = ConnectionProvider.getcon();
           String sql = "delete from workers where name=? and Service = ?";
           PreparedStatement ptst = con.prepareStatement(sql);
           ptst.setString(1,name);
           ptst.setString(2,sename);
           ptst.executeUpdate();
           JOptionPane.showMessageDialog(this, "Worker Removed !");
           showtable();
           con.close();
         }
         catch (SQLException ex) {
            Logger.getLogger(Workers.class.getName()).log(Level.SEVERE, null, ex);
        }
      }
    }//GEN-LAST:event_tableMouseClicked

