    public void load()
    {
         try {
            Connection con = ConnectionProvider.getcon();
            PreparedStatement pst = con.prepareStatement("select service_name from services");

            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                String name = rs.getString("service_name");
                c_service.addItem(name);
                c_service1.addItem(name);
                c_service2.addItem(name);
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(Workers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
